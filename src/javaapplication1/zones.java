    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed
 
     //almacenamiento de todos los ID disponibles
     DefaultListModel modelo=new DefaultListModel();
     String[]Id_array=TimeZone.getAvailableIDs();
     
     //mostrar todod los IDs habilitados
     
        for (int i = 0; i < Id_array.length; i++) {
            modelo.addElement(Id_array[i]);
            
        }
        
        lista.setModel(modelo);

        
    }//GEN-LAST:event_btnMostrarActionPerformed

    private void btnSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSeleccionarActionPerformed
    
        String pais=lista.getSelectedValue();
        String resultado="";
        java.util.TimeZone zona=java.util.TimeZone.getTimeZone(pais);
        java.util.Calendar calendario=java.util.Calendar.getInstance(zona);
        java.text.DateFormat formato= java.text.DateFormat.getDateTimeInstance();
        formato.setCalendar(calendario);
        resultado="El Tiempo en "+pais+ " es "+formato.format(new java.util.Date());
        txtResultado.setText(resultado);
        
        
        
        
        
    }//GEN-LAST:event_btnSeleccionarActionPerformed

